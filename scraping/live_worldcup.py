"""Live FIFA World Cup 2026 feed adapter.

The public API used here is optional. If it is unreachable, the application
falls back to processed local data generated by the ML pipeline.
"""

from __future__ import annotations

from datetime import datetime, timezone
from typing import Any

import pandas as pd

try:
    import requests
except ImportError:  # pragma: no cover - requests is optional before install
    requests = None


API_BASE_URL = "https://worldcup26.ir/get"


def fetch_live_games(timeout: int = 15) -> pd.DataFrame:
    """Fetch live World Cup 2026 games from the public API."""

    if requests is None:
        return pd.DataFrame()
    try:
        response = requests.get(f"{API_BASE_URL}/games", timeout=timeout)
        response.raise_for_status()
        games = response.json().get("games", [])
    except Exception:
        return pd.DataFrame()
    return normalize_games(games)


def fetch_live_teams(timeout: int = 15) -> pd.DataFrame:
    """Fetch qualified World Cup 2026 teams from the public API."""

    if requests is None:
        return pd.DataFrame()
    try:
        response = requests.get(f"{API_BASE_URL}/teams", timeout=timeout)
        response.raise_for_status()
        teams = response.json().get("teams", [])
    except Exception:
        return pd.DataFrame()
    return pd.DataFrame(
        [
            {
                "team": item.get("name_en"),
                "fifa_code": item.get("fifa_code"),
                "group": item.get("groups"),
                "team_id": str(item.get("id")),
                "flag": item.get("flag"),
            }
            for item in teams
            if item.get("name_en")
        ]
    )


def normalize_games(games: list[dict[str, Any]]) -> pd.DataFrame:
    """Normalize raw API games into dashboard-friendly columns."""

    rows = []
    for game in games:
        finished = parse_bool(game.get("finished"))
        home_score = parse_score(game.get("home_score")) if finished else None
        away_score = parse_score(game.get("away_score")) if finished else None
        local_date = parse_api_date(game.get("local_date"))
        rows.append(
            {
                "match_id": game.get("id"),
                "date": local_date,
                "home_team": game.get("home_team_name_en"),
                "away_team": game.get("away_team_name_en"),
                "home_score": home_score,
                "away_score": away_score,
                "stage": normalize_stage(game.get("type"), game.get("group")),
                "group": game.get("group"),
                "matchday": game.get("matchday"),
                "finished": finished,
                "time_elapsed": game.get("time_elapsed"),
                "status": "FINISHED" if bool(game.get("finished")) else "LIVE/UPCOMING",
            }
        )
    frame = pd.DataFrame(rows)
    if frame.empty:
        return frame
    frame["total_goals"] = frame[["home_score", "away_score"]].fillna(0).sum(axis=1)
    return frame.sort_values("date", na_position="last").reset_index(drop=True)


def parse_score(value: Any) -> int | None:
    """Parse nullable score values from string-heavy API payloads."""

    try:
        if value in {None, "", "null"}:
            return None
        return int(value)
    except (TypeError, ValueError):
        return None


def parse_bool(value: Any) -> bool:
    """Parse API booleans that may arrive as strings."""

    if isinstance(value, bool):
        return value
    return str(value).strip().lower() in {"true", "1", "yes", "y"}


def parse_api_date(value: Any) -> pd.Timestamp | pd.NaT:
    """Parse API local date into a pandas timestamp."""

    if not value:
        return pd.NaT
    return pd.to_datetime(value, format="%m/%d/%Y %H:%M", errors="coerce")


def normalize_stage(match_type: Any, group: Any) -> str:
    """Normalize API stage naming."""

    text = str(match_type or "").strip()
    stage_map = {"r32": "Round of 32", "r16": "Round of 16", "qf": "Quarterfinal", "sf": "Semifinal", "third": "Third Place", "final": "Final"}
    if text and text.lower() not in {"group", "null", "none"}:
        return stage_map.get(text.lower(), text.replace("_", " ").title())
    return f"Group {group}" if group else "Group Stage"


def live_summary(games: pd.DataFrame) -> dict[str, object]:
    """Summarize live tournament state for KPI cards."""

    if games.empty:
        return {"available": False, "total_goals": None, "remaining_matches": None, "last_update": None}
    finished = games[games["finished"]]
    remaining = games[~games["finished"]]
    return {
        "available": True,
        "total_goals": int(finished["total_goals"].sum()),
        "remaining_matches": int(len(remaining)),
        "last_update": datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC"),
    }
